package com.bmpl.projectA.users.miscdata;

public class UserMiscData {

	private int friend_request;
	private int notifications;
	private int account_messages;
	
	
	public int getFriend_request() {
		return friend_request;
	}
	public void setFriend_request(int friend_request) {
		this.friend_request = friend_request;
	}
	public int getNotifications() {
		return notifications;
	}
	public void setNotifications(int notifications) {
		this.notifications = notifications;
	}
	public int getAccount_messages() {
		return account_messages;
	}
	public void setAccount_messages(int account_messages) {
		this.account_messages = account_messages;
	}
	
	
	 
}
